/*
 * 📁 components/
 *
 * This folder holds small, reusable UI building blocks.
 * Think of components as LEGO pieces — they don't represent
 * a full page on their own, but you use them to build pages.
 *
 * WHAT GOES HERE:
 *   - Navbar.jsx       — the site navigation bar
 *   - Footer.jsx       — the site footer
 *   - Button.jsx       — a styled button used across multiple pages
 *   - Card.jsx         — a card/tile UI element
 *   - Modal.jsx        — a popup/dialog window
 *   - Header.jsx       — a page header section
 *
 * RULES FOR THIS FOLDER:
 *   1. One component per file
 *   2. File names start with a capital letter (Navbar.jsx, not navbar.jsx)
 *   3. Components here should NOT contain full-page logic — keep them small
 *   4. If a component is only used on ONE page, it can live in that page's
 *      file instead. Move it here when it's used in 2+ places.
 *
 * EXAMPLE:
 *   export default function Button({ label, onClick }) {
 *     return <button onClick={onClick}>{label}</button>;
 *   }
 */

